package com.moayad.deal_with_character;

public class UpperAndLowerCases {

	public static void main(String[] args) {
		char letter1 = 'E';
		char letter2 = 'm';
		
		letter1 = Character.toLowerCase(letter1);
		System.out.println(letter1);
		
		letter2 = Character.toUpperCase(letter2);
		System.out.println(letter2);

	}

}
